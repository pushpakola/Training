package com.dl.one.selectionstatements;

public class Eg4 {

	//work on student grade system
	public static void main(String[] args) {
		
		float salary = 70000;
		
		if (salary == 10000f) {
			System.out.println("User Salary is: " + salary);
		}else if (salary == 20000f) {
			System.out.println("User Salary is: " + salary);
		}else if (salary == 30000f) {
			System.out.println("User Salary is: " + salary);
		}else if (salary == 40000f) {
			System.out.println("User Salary is: " + salary);
		}else {
			System.out.println("User Salary is out of range " + salary);
		}
	}
}
