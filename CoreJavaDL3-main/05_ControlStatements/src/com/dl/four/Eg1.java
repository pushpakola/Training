package com.dl.four;

public class Eg1 {

	public static void main(String[] args) {
		
		int i = 2;
		if(i%2==0) {
			System.out.println("Even Number " + i);
		}else {
			System.out.println("Odd Number " + i);
		}
		
		int j = 3;
		if(j%2!=0) {
			System.out.println("Odd Number " + j);
		}else {
			System.out.println("Even Number " + j);
		}
		
	}
}
