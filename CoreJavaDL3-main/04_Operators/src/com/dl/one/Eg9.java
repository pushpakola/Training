package com.dl.one;

public class Eg9 {

	public static void main(String[] args) {
		
		int a = 10; // '0b1010'
		int b = 2; // '0b0010'
		
		System.out.println(a<<b); // 40

		//0b00001010
		
		//0b00101000
		
		int x = 8; // '0b1000'
		int y = 4; // 0b0100
		
		System.out.println(x<<y);
		
		//0b00001000
		//0b10000000
		
	}

}
