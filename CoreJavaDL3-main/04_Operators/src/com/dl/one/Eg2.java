package com.dl.one;

public class Eg2 {

	public static void main(String[] args) {
		
		int a = 5;
		int b = 15;
		System.out.println(a+=1); // 6 // a = a+1
		System.out.println(b+=1); // 16 
		
		System.out.println(a-=1); // 5 // a = a-1 // 6 -1 // 5
		System.out.println(b-=1); // 15
		
		System.out.println(a*=1); // 5
		System.out.println(b*=2); // 30
		
		System.out.println(a/=10); // 0
		
		System.out.println(b%=3); // 0
		 
	}
}
