package com.dl.one;

public class Eg3 {

	public static void main(String[] args) {
		
		int a = 10; // decimals
		System.out.println(a); 
		
		int b = 020; // octal // 8 , 0 to 7
		System.out.println(b);
		
		int c = 0xace; // hexa // 16 , a to f, 0 t0 9
		System.out.println(c);
		
		int d = 0b01010101; // bin // 2 // 01
		System.out.println(d);
		
		
		char ch = 'a';
		int i = ch;
		System.out.println(i);
		
		
		
	}
}
