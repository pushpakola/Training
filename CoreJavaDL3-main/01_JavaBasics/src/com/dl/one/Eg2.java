package com.dl.one;

public class Eg2 {

	public static void main(String[] args) {
		
		byte b = 127;
		System.out.println(b);
		
		short s = 3277;
		System.out.println(s);
		
		int i = 23456;
		System.out.println(i);
		
		long l = 1234566789;
		System.out.println(l);
		
		float f1 = 10.250f;
		System.out.println(f1);
		
		double d1 = 20.250;
		System.out.println(d1);
		
		boolean b1 = true;
		System.out.println(b1);
		
		char ch = 'A';
		System.out.println(ch);
		
	}

}
